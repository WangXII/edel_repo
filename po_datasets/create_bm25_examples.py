def get_count_dict(
    keyword_list: List[Tuple[str, List]], default_value: Any = 0
) -> Dict[str, Any]:
    count_dict: Dict[str, int] = {}
    for keyword in keyword_list:
        count_dict[keyword[0]] = default_value
    return count_dict


def get_label_margin_dict(
    keyword_list: List[Tuple[str, List]], margin_fn: Callable
) -> Dict[str, Tuple[int, float]]:
    label_margin_dict: dict[str, Tuple[int, float]] = {}
    for keyword in keyword_list:
        if keyword[0][0] == ">":
            label = 0
        elif keyword[0][0] == "<":
            label = 1
        else:
            raise ValueError(f"Label {keyword[0]} is not a valid label")
        margin_value = float(keyword[0][2:])
        label_margin_dict[keyword[0]] = (label, margin_fn(margin_value))
    return label_margin_dict


