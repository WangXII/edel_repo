    def get_variant_keywords(self, variant):
        keywords = []
        keywords.append(variant)
        # Fusion variant, e.g., "ALK::EML4"
        if "::" in variant:
            gene1, gene2 = variant.split("::")
            keywords.append(gene1 + " fusion")
            keywords.append(gene2 + " fusion")
        # Variant with both Amino Acid and Nucleotide substitution, e.g., "L858R (c.2573T>G)"
        elif "(" in variant:
            keywords.append(variant.split("(")[0])
            # Add the nucleotide substitution
            keywords.append(variant.split("(")[1].replace(")", ""))
        return keywords

    def str_entity_mapping(self, text, example) -> list:
        if text == "gene" and not self.use_gene_synonyms:
            return [[example["gene"]]]
        elif text == "gene" and self.use_gene_synonyms:
            return [example["gene_synonyms"]]
        elif text == "gene_synonyms":
            return [example["gene_synonyms"]]
        elif text == "variant":
            return [[example["variant"]]]
        elif text == "disease":
            return [example["disease"] + self.cancer_keywords]
        elif text == "drug_keywords":
            return [self.drug_keywords]
        elif text == "gene_keywords":
            return [self.cancer_genes_keywords]
        elif text == "variant_keywords":
            return [self.cancer_variants_keywords]
        elif text == "drugs":
            drugs = example["drugs"]
            drugs_synonyms = []
            for drug in drugs:
                synonyms = [drug]
                synonym_list = list(
                    self.nci_thesaurus.mapper.loc[
                        self.nci_thesaurus.mapper["display name"] == drug
                    ]["synonyms"]
                )
                if len(synonym_list) > 0:
                    for synonym in synonym_list[0].split("|"):
                        synonyms.append(synonym)
                drugs_synonyms.append(synonyms)
            drugs_entities = drugs if not self.use_drug_synonyms else drugs_synonyms
            return drugs_entities

